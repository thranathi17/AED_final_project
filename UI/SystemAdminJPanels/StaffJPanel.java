    private void addStaffBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addStaffBtnActionPerformed
       String firstName = staffFNameTextField.getText();
       String lastName = staffLNameTextField.getText();
       String phone = staffPhone.getText();
       String email = staffEmail.getText();
       String address = staffAddress.getText();
       String role = staffRoleCBox.getSelectedItem().toString();
       Float experience = Float.valueOf(staffExp.getText());
       String userName = staffUName.getText();
       String password = staffPassword.getText();
       
       String hospName = hospCBox.getSelectedItem().toString();
       
       Role currentRole;
       
       if (role.equals("Nurse"))
       {
           currentRole = new NurseRole();
       }
       else
       {
           currentRole = new ReceptionistRole();
       }
       
       Hospital hospital = this.business.getHospitals().findHospitalByName(hospName);
       StaffDirectory staff = hospital.getStaff();
       Staff s = staff.addStaffMember(hospital, firstName, lastName, email, phone, address, currentRole, experience);
       
       //create a useraccount and add it to the top level dir
        UserAccount ua = new UserAccount();
        ua.setUsername(userName);
        ua.setPassword(password);
        ua.setRole(currentRole);
        ua.setPersonId(s.getId());
        //staff.setUserAcc(ua);

        this.business.getTopLevelUserAccountDirectory().getUseraccountList().add(ua);
        displayStaffMembers();
       
       JOptionPane.showMessageDialog(null, "Doctor registered successfully!");
    }//GEN-LAST:event_addStaffBtnActionPerformed


