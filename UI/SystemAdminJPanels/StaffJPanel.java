    private void populateHospitals()
    {
        for(Hospital b : this.business.getHospitals().getHospitals())
        {
            hospCBox.addItem(b.getHospitalName());
        }
    }
    
    public void displayStaffMembers()
    {
        ArrayList<Hospital> hospitals = this.business.getHospitals().getHospitals();
        
        if(hospitals.size() >= 0) {
            staffTableModel.setRowCount(0);
            for(Hospital h: hospitals) {
                for(Staff d : h.getStaff().getStaffList())
                {
                    Object row[] = new Object[5];
                    row[0] = d.getId();
                    row[1] = d.getFirstName() + ' ' + d.getLastName();
                    row[2] = d.getRole();
                    row[3] = d.getPhoneNumber();
                    row[4] = h.getHospitalName();
                    staffTableModel.addRow(row);
                }
            }
        }
        
    }

