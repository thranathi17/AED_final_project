    public void displayTestLists(){
         
          ArrayList<TestRequest> trs = this.business.getTestPerformRequests().getRequests();
        if(trs.size() >= 0) {
            testListTableModel.setRowCount(0);
            for(TestRequest r: trs) {
                Object row[] = new Object[6];
                row[0] = r.getTestId();
                row[1] = r.getDoctor().getFirstName()+" "+r.getDoctor().getLastName();
                row[2] = r.getPatient().getId();
                row[3] = r.getPatient().getFirstName() + ' '+ r.getPatient().getLastName();
                row[4] = r.getLabTests().getProcedureName();
                row[5] = r.getStatus();
                
                testListTableModel.addRow(row);
            }
        } 
             }

